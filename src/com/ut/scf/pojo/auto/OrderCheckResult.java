package com.ut.scf.pojo.auto;

import java.util.ArrayList;

public class OrderCheckResult {

	private String batch_id;
	
	private String date;
	private ArrayList<String> refuse_order_id;
	public String getBatch_id() {
		return batch_id;
	}
	public void setBatch_id(String batch_id) {
		this.batch_id = batch_id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public ArrayList<String> getRefuse_order_id() {
		return refuse_order_id;
	}
	public void setRefuse_order_id(ArrayList<String> refuse_order_id) {
		this.refuse_order_id = refuse_order_id;
	}

}